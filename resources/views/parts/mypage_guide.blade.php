    <div class="guide-container">
        <h3 class="guide-title">【利用説明】</h3>
        <h4 class="guide-sub-title">マイページについて</h4>
        <ul class="guide-list">
            <li>そのユーザーの自己紹介文やコンテンツ、投稿を見ることができます。</li>
            <li>「フォロー中」、「フォロワー」の表示を押すことでそれぞれのユーザーを確認することができます。</li>
            <li>「いいね一覧」からそのユーザーがいいねした投稿を確認することができます。</li>
            <li>BigPost画面ではビッグポストとして設定された重要な投稿のみを見ることができます。</li>
            <li>Post画面、BigPost画面ではそれぞれの投稿をマイカテゴリーで絞り込むことができます。</li>
        </ul>
        <h4 class="guide-sub-title">自分のマイページでのみできること</h4>
        <ul class="guide-list">
            <li>「プロフィール編集」から自分のプロフィールを編集することができます。</li>
            <li>「マイカテゴリー設定」からマイカテゴリーの追加・編集をすることができます。</li>
            <li>Main画面の「メインコンテンツを作成する」から自分のメインコンテンツを作成する事ができます。</li>
            <li>Store画面の「メインコンテンツを作成する」から自分のメインコンテンツを作成する事ができます。</li>
        </ul>
        <h4 class="guide-sub-title">他ユーザーのマイページでのみできること</h4>
        <ul class="guide-list">
            <li>「フォロー」ボタンを押すことでそのユーザーをフォローすることができます。フォロー解除の際も同様です。</li>
        </ul>
        <h4 class="guide-sub-title">メインコンテンツについて</h4>
        <ul class="guide-list">
            <li>自身の過去の経歴や実績、または現在の活動や頑張っていることなどを書くことで、自身の紹介をすることができます。</li>
            <li>画像を4枚まで添付したり、URLを貼ったりすることができます。</li>
            <li>メインコンテンツの数に制限は無く、いくつでも作ることができます。</li>
            <li>自身のメインコンテンツでは、「<i class="fa-solid fa-ellipsis"></i>」を押すことでメニューが表示され、そこから編集・削除をすることができます。</li>
        </ul>
        <h4 class="guide-sub-title">投稿について</h4>
        <ul class="guide-list">
            <li>自身の日々の体験や成果、つぶやき、お知らせなど、好きなことを書いて投稿することができます。</li>
            <li>画像を4枚まで添付したり、URLを貼ったりできるほか、投稿は「#」でタグを付けることもできます。</li>
            <li>投稿の数に制限は無く、いくつでも作ることができます。</li>
            <li>自身のメインコンテンツでは、「<i class="fa-solid fa-ellipsis"></i>」を押すことでメニューが表示され、そこから削除をすることができます。</li>
            <li>投稿は編集することができません。そのため、日々の頑張りや出来事の証拠として残しておくことができます。</li>
        </ul>
        <h4 class="guide-sub-title">ストアコンテンツについて</h4>
        <ul class="guide-list">
            <li>物品の販売や依頼の受注、自身が提供しているサービスなど、自身の収益活動について紹介することができます。</li>
            <li>画像を4枚まで添付したり、URLを貼ったりすることができます。</li>
            <li>ストアコンテンツの数に制限は無く、いくつでも作ることができます。</li>
            <li>自身のストアコンテンツでは、「<i class="fa-solid fa-ellipsis"></i>」を押すことでメニューが表示され、そこから編集・削除をすることができます。</li>
        </ul>
    </div>
</aside>